#import <Foundation/Foundation.h>

int main(int argc, const char * argv[]) {
    @autoreleasepool {

        //   0. 1代表赢，0代表输,由比赛结果决定
        float Sa = 1;
        float Sb = 1 - Sa ;
        
        //   1.a,b分数
        float Ra = 1400 ;
        float Rb = 1500 ;
        
        
//        scanf("%f %f",&Ra,&Rb);
        
        printf("a = %f \nb = %f",Ra, Rb);
        
        //  2. 分差
        float Dab = Ra - Rb ;
        float Dba = Rb - Ra ;
        
        
        //  3.胜率の分母の指数
        float indexA = -Dab  /  400.00 ;
        float indexB = -Dba  /  400.00 ;
        
        //  4.
        //  4.1 A对B的期望胜率
        float Ea = 1  / ( 1 + pow(10, indexA) );
        float Eb = 1  / ( 1 + pow(10, indexB) );
        
        //  5. A胜B，B胜A的概率
        float Pab = Ea ;
        float Pba = Eb ;
        
        //  6. 最终比分
        float Wa =  Sa ;
        float Wea = Pab ;
        
        float Wb =  Sb ;
        float Web = Pba ;
        
        
        
        float K = 30.0 ;
        float RaFianl = Ra + K * ( Wa - Wea ) ;
        
        float RbFianl = Rb + K * ( Wb - Web ) ;
    
        printf("\na的最终得分是：%f \na的胜率是%f%%\n",RaFianl,Ea);
        
        printf("\nb的最终得分是：%f \nb的胜率是%f%%\n",RbFianl,Eb);
        
    }
    return 0;
}





